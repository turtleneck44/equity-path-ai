import { TrendingUp, TrendingDown, Minus, Brain, Target, Zap } from "lucide-react";
import { Card } from "@/components/ui/card";
import { Badge } from "@/components/ui/badge";
import { Progress } from "@/components/ui/progress";
import { cn } from "@/lib/utils";

interface PredictionData {
  symbol: string;
  currentPrice: number;
  predictedPrice: number;
  timeframe: string;
  confidence: number;
  trend: "bullish" | "bearish" | "neutral";
  changePercent: number;
  explanation?: string;
}

interface PredictionCardProps {
  data: PredictionData;
  className?: string;
}

export function PredictionCard({ data, className }: PredictionCardProps) {
  const {
    symbol,
    currentPrice,
    predictedPrice,
    timeframe,
    confidence,
    trend,
    changePercent,
    explanation
  } = data;

  const isPositive = changePercent > 0;
  const isNeutral = Math.abs(changePercent) < 0.5;

  const getTrendIcon = () => {
    if (isNeutral) return Minus;
    return isPositive ? TrendingUp : TrendingDown;
  };

  const getTrendColor = () => {
    if (isNeutral) return "text-chart-neutral";
    return isPositive ? "text-chart-bull" : "text-chart-bear";
  };

  const getTrendBg = () => {
    if (isNeutral) return "bg-chart-neutral/10";
    return isPositive ? "bg-chart-bull/10" : "bg-chart-bear/10";
  };

  const TrendIcon = getTrendIcon();

  return (
    <Card className={cn("luxury-card p-6 space-y-6", className)}>
      {/* Header */}
      <div className="flex items-center justify-between">
        <div className="flex items-center space-x-3">
          <div className="flex items-center justify-center w-12 h-12 bg-gradient-primary rounded-xl">
            <Brain className="w-6 h-6 text-primary-foreground" />
          </div>
          <div>
            <h3 className="text-xl font-bold text-foreground">{symbol}</h3>
            <p className="text-sm text-muted-foreground">AI Prediction â€¢ {timeframe}</p>
          </div>
        </div>
        <Badge 
          className={cn(
            "px-3 py-1 font-medium",
            getTrendBg(),
            getTrendColor()
          )}
        >
          {trend.charAt(0).toUpperCase() + trend.slice(1)}
        </Badge>
      </div>

      {/* Price Prediction */}
      <div className="space-y-4">
        <div className="grid grid-cols-2 gap-4">
          <div className="space-y-2">
            <p className="text-sm text-muted-foreground">Current Price</p>
            <p className="text-2xl font-bold text-foreground">
              ${currentPrice.toFixed(2)}
            </p>
          </div>
          <div className="space-y-2">
            <p className="text-sm text-muted-foreground">Predicted Price</p>
            <p className={cn(
              "text-2xl font-bold",
              getTrendColor()
            )}>
              ${predictedPrice.toFixed(2)}
            </p>
          </div>
        </div>

        {/* Change Indicator */}
        <div className={cn(
          "flex items-center justify-center p-4 rounded-xl",
          getTrendBg()
        )}>
          <TrendIcon className={cn("w-6 h-6 mr-2", getTrendColor())} />
          <span className={cn("text-lg font-bold", getTrendColor())}>
            {isPositive ? "+" : ""}{changePercent.toFixed(2)}%
          </span>
          <span className="text-sm text-muted-foreground ml-2">
            ({isPositive ? "+" : ""}${(predictedPrice - currentPrice).toFixed(2)})
          </span>
        </div>
      </div>

      {/* Confidence Score */}
      <div className="space-y-3">
        <div className="flex items-center justify-between">
          <div className="flex items-center space-x-2">
            <Target className="w-4 h-4 text-primary" />
            <span className="text-sm font-medium text-foreground">Confidence Score</span>
          </div>
          <span className="text-sm font-bold text-primary">{confidence}%</span>
        </div>
        <Progress 
          value={confidence} 
          className="h-2 bg-muted/50"
        />
        <div className="flex justify-between text-xs text-muted-foreground">
          <span>Low</span>
          <span>High</span>
        </div>
      </div>

      {/* AI Explanation */}
      {explanation && (
        <div className="space-y-3 pt-4 border-t border-border/50">
          <div className="flex items-center space-x-2">
            <Zap className="w-4 h-4 text-secondary" />
            <span className="text-sm font-medium text-foreground">AI Analysis</span>
          </div>
          <p className="text-sm text-muted-foreground leading-relaxed">
            {explanation}
          </p>
        </div>
      )}

      {/* Disclaimer */}
      <div className="text-xs text-muted-foreground bg-muted/30 p-3 rounded-lg">
        <strong>Disclaimer:</strong> This prediction is generated by AI and should not be considered as financial advice. 
        Always do your own research before making investment decisions.
      </div>
    </Card>
  );
}